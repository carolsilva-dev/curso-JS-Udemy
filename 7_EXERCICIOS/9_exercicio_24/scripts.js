// Escreva uma função que receba dois números, o primeiro é a base e o segundo a potencia
// Depois faça essa operação e retorne o resultado
// Por exemplo: 3,2 = 9


function potencia(a,b) {    // poderia ter feito de outra forma usando o modo return Math.pow(a,b);
  return a ** b;
}

console.log(potencia(2,2));
console.log(potencia(3,2));
console.log(potencia(4,4));
console.log(potencia(5,2));